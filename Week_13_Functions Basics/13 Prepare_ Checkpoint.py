def display_regular(text):
  text1 = text
  print(text1)

def display_uppercase(text):
  text2 = text.upper()
  print(text2)

def display_lowercase(text):
  text3 = text.lower()
  print(text3)

word = input("What is your message? ")

display_regular(word)
display_uppercase(word)
display_lowercase(word)

